    void LanguageSettings(int enOrLt) {
        Language language = new Language();
        choice = enOrLt;
        if (enOrLt == 1) {
            jLabel1.setText(language.English[16]);
            jLabel2.setText(language.English[17]);
            jLabel3.setText(language.English[18]);
            jButton1.setText(language.English[15]);
        } else {
            jLabel1.setText(language.Lithuanian[16]);
            jLabel2.setText(language.Lithuanian[17]);
            jLabel3.setText(language.Lithuanian[18]);
            jButton1.setText(language.Lithuanian[15]);
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        codeOfGroup = codeofgroup.getText();
        studyProgram = studyprogram.getText();
        if (NumberUtils.isNumber(entranceyear.getText()) && !codeOfGroup.isEmpty() && !studyProgram.isEmpty()) {
            entranceYear = Integer.parseInt(entranceyear.getText());
            try {
                String sql = String.format("INSERT INTO Group (Code, [Study program], [Study entrance year]) VALUES('"+codeOfGroup+"', '"+studyProgram+"', "+entranceYear+")");        
                con.execute(sql);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Group.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            if (choice == 1)
                 JOptionPane.showMessageDialog(this, "You must fill all fields!\nEntance year must be integer!", "Error", JOptionPane.ERROR_MESSAGE);
            else JOptionPane.showMessageDialog(this, "Reikia u탑pildyti visus laukus!\nStudijos prad탑ia - turi b큰ti skai훾ius", "Klaida", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

